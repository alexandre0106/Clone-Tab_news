def jogo_perguntas():
    # Perguntas e respostas
    perguntas = {
        "Qual é o maior planeta do nosso sistema solar?": "Júpiter",
        "Quem pintou a famosa obra 'A Mona Lisa'?": "Leonardo da Vinci",
        "Qual é o maior oceano do mundo?": "Oceano Pacífico",
        "Em que ano o Brasil se tornou independente?": "1822"
    }
    
    # Contador de acertos
    acertos = 0
    
    # Loop pelas perguntas
    for pergunta, resposta_correta in perguntas.items():
        print(pergunta)
        resposta_usuario = input("Sua resposta: ").strip()
        
        # Verifica se a resposta do usuário está correta
        if resposta_usuario.lower() == resposta_correta.lower():
            print("Resposta correta!")
            acertos += 1
        else:
            print(f"Resposta errada! A resposta correta é: {resposta_correta}")
    
    # Final do jogo
    print(f"\nVocê acertou {acertos} de {len(perguntas)} perguntas!")

# Chama a função para iniciar o jogo
jogo_perguntas()
